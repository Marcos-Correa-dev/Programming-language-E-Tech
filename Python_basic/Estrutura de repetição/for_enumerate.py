# Percorrendo uma lista com índices
frutas = ["maçã", "banana", "laranja", "uva"]
for indice, fruta in enumerate(frutas):
    print(f"A fruta no índice {indice} é {fruta}")

# `enumerate()` retorna tanto o índice quanto o valor de
# cada elemento da lista.
# Isso é útil quando precisamos trabalhar com a posição dos
# elementos.


